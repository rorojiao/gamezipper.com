// Build a grid for a level: 0=empty/white, 1-4=triangle, -1=black
function buildGrid(level) {
  const {w, h, blacks, solution} = level;
  const grid = Array.from({length: h}, () => Array(w).fill(0));

  // Mark black cells
  for (const [r, c, num] of blacks) {
    grid[r][c] = -1; // black
  }

  // Place triangles from solution
  for (const [r, c, t] of solution) {
    if (grid[r][c] === -1) {
      console.log(`  ERROR: solution places triangle at black cell (${r},${c})`);
    }
    grid[r][c] = t;
  }

  return grid;
}

// Check number constraints
function checkNumbers(level) {
  const {w, h, blacks, solution} = level;
  const grid = buildGrid(level);
  const errors = [];

  // Build solution map for quick lookup
  const solMap = new Set();
  for (const [r, c, t] of solution) {
    solMap.add(`${r},${c}`);
  }

  const dirs = [[-1,0],[1,0],[0,-1],[0,1]];

  for (const [r, c, num] of blacks) {
    if (num < 0) continue; // no number hint

    let count = 0;
    for (const [dr, dc] of dirs) {
      const nr = r + dr, nc = c + dc;
      if (nr >= 0 && nr < h && nc >= 0 && nc < w) {
        if (solMap.has(`${nr},${nc}`)) count++;
      }
    }

    if (count !== num) {
      errors.push(`Black cell (${r},${c}) has number ${num} but ${count} adjacent triangles`);
    }
  }

  return errors;
}

// Check rectangle constraint for white areas
// A "white area" is a connected region of cells that are:
// - not black
// - not containing a triangle (type 0)
// We need each connected region to be a rectangle
function checkRectangles(level) {
  const grid = buildGrid(level);
  const {w, h} = level;
  const visited = Array.from({length: h}, () => Array(w).fill(false));
  const errors = [];

  function floodFill(r, c) {
    const cells = [];
    const queue = [[r, c]];
    visited[r][c] = true;

    while (queue.length > 0) {
      const [cr, cc] = queue.shift();
      cells.push([cr, cc]);

      for (const [dr, dc] of [[-1,0],[1,0],[0,-1],[0,1]]) {
        const nr = cr + dr, nc = cc + dc;
        if (nr >= 0 && nr < h && nc >= 0 && nc < w && !visited[nr][nc] && grid[nr][nc] === 0) {
          visited[nr][nc] = true;
          queue.push([nr, nc]);
        }
      }
    }

    return cells;
  }

  function isRectangle(cells) {
    if (cells.length === 0) return true;

    const rows = cells.map(c => c[0]);
    const cols = cells.map(c => c[1]);
    const minR = Math.min(...rows), maxR = Math.max(...rows);
    const minC = Math.min(...cols), maxC = Math.max(...cols);

    const expectedCount = (maxR - minR + 1) * (maxC - minC + 1);
    if (cells.length !== expectedCount) return false;

    // Check all cells in the bounding box are in the set
    const cellSet = new Set(cells.map(c => `${c[0]},${c[1]}`));
    for (let r = minR; r <= maxR; r++) {
      for (let c = minC; c <= maxC; c++) {
        if (!cellSet.has(`${r},${c}`)) return false;
      }
    }

    return true;
  }

  for (let r = 0; r < h; r++) {
    for (let c = 0; c < w; c++) {
      if (!visited[r][c] && grid[r][c] === 0) {
        const region = floodFill(r, c);
        if (!isRectangle(region)) {
          errors.push(`Non-rectangular white region at starting (${r},${c}) with ${region.length} cells`);
        }
      }
    }
  }

  return errors;
}

// Full check for each level
let totalErrors = 0;
for (let i = 0; i < LEVELS.length; i++) {
  const numErrors = checkNumbers(LEVELS[i]);
  const rectErrors = checkRectangles(LEVELS[i]);

  if (numErrors.length > 0 || rectErrors.length > 0) {
    console.log(`\n=== Level ${i+1} (size ${LEVELS[i].w}x${LEVELS[i].h}) ===`);
    numErrors.forEach(e => console.log(`  NUM: ${e}`));
    rectErrors.forEach(e => console.log(`  RECT: ${e}`));
    totalErrors++;
  } else {
    console.log(`Level ${i+1}: OK`);
  }
}

